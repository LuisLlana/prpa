import math 

def circle(radius):
    """
    Function that computes the surface of a circle given its radius.
    
    Parameters
    ----------
    radius: float
      The radius of the circle
      
    
    Returns
    -------
    float
      The surface of the circle
    """
    return math.pi * radius**2
    
    

    
    
    
